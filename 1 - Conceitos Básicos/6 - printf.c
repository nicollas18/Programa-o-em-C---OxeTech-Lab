#include <stdio.h>

int main() 
{
    // Declaração e inicialização das variáveis
    int numero = 42;         // Variável inteira
    char letra = 'A';        // Variável caractere
    float real = 3.14;       // Variável float

    // Impressão dos valores das variáveis usando printf
    printf("O valor da variável numero é: %d\n", numero);
    printf("O valor da variável letra é: %c\n", letra);
    printf("O valor da variável real é: %.2f\n", real);

    return 0;
}